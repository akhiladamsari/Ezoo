package model;

import java.sql.Time;
import java.util.Date;

/**
 *
 * @author Akhila Damsari
 */
public class Comment {

    private int userID;
    private int commentID;
    private int logID;
    private Date date;
    private Time time;
    private String content;

    public Comment() {
    }

    public Comment(int userID, int commentID, int logID, Date date, Time time, String content) {
        this.userID = userID;
        this.commentID = commentID;
        this.logID = logID;
        this.date = date;
        this.time = time;
        this.content = content;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public int getCommentID() {
        return commentID;
    }

    public void setCommentID(int commentID) {
        this.commentID = commentID;
    }

    public int getLogID() {
        return logID;
    }

    public void setLogID(int logID) {
        this.logID = logID;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public Time getTime() {
        return time;
    }

    public void setTime(Time time) {
        this.time = time;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
    
    

}
